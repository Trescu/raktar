CREATE TABLE IF NOT EXISTS "leltar" (
	"cikkszam"	TEXT NOT NULL,
	"raktarID"	INTEGER NOT NULL,
	"idopont"	TEXT NOT NULL,
	"felelos"	TEXT NOT NULL,
	"mennyiseg"	INTEGER NOT NULL,
	"valos_mennyiseg"	INTEGER,
	PRIMARY KEY("cikkszam"),
