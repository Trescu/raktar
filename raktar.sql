BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "armatrix" (
	"cikkszam" TEXT PRIMARY KEY,
	"lista_ar"	INTEGER NOT NULL,
	"akcios_ar"	INTEGER
);
CREATE TABLE IF NOT EXISTS "butor" (
	"cikkszam"	TEXT NOT NULL,
	"nev"	TEXT NOT NULL,
	"suly"	REAL NOT NULL,
	"x"	INTEGER NOT NULL,
	"y"	INTEGER NOT NULL,
	"z"	INTEGER NOT NULL,
