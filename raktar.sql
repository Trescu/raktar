CREATE TABLE IF NOT EXISTS "raklap" (
	"szam"	INTEGER NOT NULL,
	"mennyiseg"	INTEGER NOT NULL,
	"tipusID"	INTEGER NOT NULL,
	PRIMARY KEY("szam"),
	FOREIGN KEY("tipusID") REFERENCES "tipus"("id")
);
CREATE TABLE IF NOT EXISTS "rakt_biz_fej" (
	"bizszam"	INTEGER NOT NULL,
	"datum"	TEXT NOT NULL,
	"felelos"	TEXT NOT NULL,
	"raktarID"	INTEGER NOT NULL,
	PRIMARY KEY("bizszam" AUTOINCREMENT),
	FOREIGN KEY("raktarID") REFERENCES "raktar"("id")
);
CREATE TABLE IF NOT EXISTS "rakt_biz_tetel" (
	"bizszam"	INTEGER NOT NULL,
	"tetelid"	INTEGER NOT NULL,
	"cikkszam"	TEXT NOT NULL,
	"mennyiseg"	INTEGER NOT NULL,
	"ar"	INTEGER NOT NULL,
	PRIMARY KEY("bizszam","tetelid"),
	FOREIGN KEY("bizszam") REFERENCES "rakt_biz_fej"("bizszam"),
	FOREIGN KEY("cikkszam") REFERENCES "butor"("cikkszam")
);
CREATE TABLE IF NOT EXISTS "raktar" (
	"id"	INTEGER NOT NULL,
	"nev"	TEXT NOT NULL,
	"pozicio"	TEXT NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "raktarak_raktari_poziciok" (
	"raktar_poz_ID"	INTEGER NOT NULL,
	"raktar_ID"	INTEGER NOT NULL,
	FOREIGN KEY("raktar_ID") REFERENCES "raktar"("id"),
	FOREIGN KEY("raktar_poz_ID") REFERENCES "raktari_pozicio"("id")
);
CREATE TABLE IF NOT EXISTS "raktari_pozicio" (
	"id"	INTEGER NOT NULL,
	"sor"	INTEGER NOT NULL,
	"oszlop"	INTEGER NOT NULL,
	"polc"	INTEGER NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "raktarkeszlet" (
	"raktarID"	INTEGER NOT NULL,
	"cikkszam"	TEXT NOT NULL,
	"mennyiseg"	INTEGER NOT NULL,
	FOREIGN KEY("cikkszam") REFERENCES "butor"("cikkszam"),
	FOREIGN KEY("raktarID") REFERENCES "raktar"("id")
);
CREATE TABLE IF NOT EXISTS "tipus" (
	"id"	INTEGER NOT NULL,
	"nev"	TEXT NOT NULL,
	"netto_suly"	REAL NOT NULL,
	"brutto_suly"	REAL NOT NULL,
	"x"	INTEGER NOT NULL,
	"y"	INTEGER NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
